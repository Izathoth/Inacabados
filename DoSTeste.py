    def display_status(self, status_code, data):
        with self.lock:
            if status_code == 200:
                self.success_count += 1
                print(f"{Colors.SUCCESS}[SUCESSO] Requisição para: {self.url} | Status: {status_code} | Tamanho do arquivo: {self.file_size} bytes | Dados: {data[:50]}...{Colors.RESET}")
            elif status_code == "Erro":
                self.error_count += 1
                print(f"{Colors.ERROR}[ERRO] Requisição para: {self.url} | Status: {status_code} | Tamanho do arquivo: {self.file_size} bytes | Dados: {data[:50]}...{Colors.RESET}")
