    def scan_open_ports(self):
        open_ports = []
        for port in range(1, 65535):
            sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
            result = sock.connect_ex((self.url, port))
            if result == 0:
                open_ports.append(port)
            sock.close()
        return open_ports

